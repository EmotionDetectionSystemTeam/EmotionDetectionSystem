class ClassDisplay {
    public id: number;
    public name: string;
    public date: string;
    public description: string;
  
    constructor(id: number, name: string, date: string, description: string) {
      this.id = id;
      this.name = name;
      this.date = date;
      this.description = description;
    }
  }

  export default ClassDisplay;